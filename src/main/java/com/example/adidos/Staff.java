package com.example.adidos;

import java.sql.Date;

public class Staff {
	private String staffID;
	private String staffName;
	private String staffPhone;
	private String staffEmail;
	private Date staffBirthDate;
	private String staffAddress; 
	private String staffCity; 
	private int staffPostcode; 
	private String staffState;
	private String staffGender;
	private String staffNationality;
	private String staffMaritalStatus;
	private String staffReligion;
	private String staffPassword;

	public Staff(){}

	public String getStaffID() {
		return staffID;
	}

	public void setStaffID(String staffID) {
		this.staffID = staffID;
	}

	public String getStaffName() {
		return staffName;
	}

	public void setStaffName(String staffName) {
		this.staffName = staffName;
	}

	public String getStaffPhone() {
		return staffPhone;
	}

	public void setStaffPhone(String staffPhone) {
		this.staffPhone = staffPhone;
	}

	public String getStaffEmail() {
		return staffEmail;
	}

	public void setStaffEmail(String staffEmail) {
		this.staffEmail = staffEmail;
	}

	public Date getStaffBirthDate() {
		return staffBirthDate;
	}

	public void setStaffBirthDate(Date staffBirthDate) {
		this.staffBirthDate = staffBirthDate;
	}

	public String getStaffAddress() {
		return staffAddress;
	}

	public void setStaffAddress(String staffAddress) {
		this.staffAddress = staffAddress;
	}

	public String getStaffCity() {
		return staffCity;
	}

	public void setStaffCity(String staffCity) {
		this.staffCity = staffCity;
	}

	public int getStaffPostcode() {
		return staffPostcode;
	}

	public void setStaffPostcode(int staffPostcode) {
		this.staffPostcode = staffPostcode;
	}

	public String getStaffState() {
		return staffState;
	}

	public void setStaffState(String staffState) {
		this.staffState = staffState;
	}

	public String getStaffGender() {
		return staffGender;
	}

	public void setStaffGender(String staffGender) {
		this.staffGender = staffGender;
	}

	public String getStaffNationality() {
		return staffNationality;
	}

	public void setStaffNationality(String staffNationality) {
		this.staffNationality = staffNationality;
	}

	public String getStaffMaritalStatus() {
		return staffMaritalStatus;
	}

	public void setStaffMaritalStatus(String staffMaritalStatus) {
		this.staffMaritalStatus = staffMaritalStatus;
	}

	public String getStaffReligion() {
		return staffReligion;
	}

	public void setStaffReligion(String staffReligion) {
		this.staffReligion = staffReligion;
	}

	public String getStaffPassword() {
		return staffPassword;
	}

	public void setStaffPassword(String staffPassword) {
		this.staffPassword = staffPassword;
	}
}